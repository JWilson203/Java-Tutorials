class Arithmetic{
	public static void main (String[] args){
	int num = 100;
	int factor = 20;
	int sum = 0;

	sum = num + factor;
	System.out.println("Additon Sum: " + sum);
	sum = num - factor;
	System.out.println("Subtraction Sum: " + sum);

	sum = num * factor;
	System.out.println("Multiplication Sum: " + sum);
	sum = num / factor;
	System.out.println("Division Sum: " + sum);
	}
}